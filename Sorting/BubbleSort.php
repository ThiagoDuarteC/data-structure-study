function bubble_sort(array &$array) {
    for ($i = 0; $i < count($array); $i++) {
        $changed = false;
        for ($j = 0; $j < count($array) - $i - 1; $j++) {
            if ($array[$j] > $array[$j + 1]){
                [$array[$j], $array[$j + 1]] = [$array[$j + 1], $array[$j]];
                $changed = true;
            }
        }
        if(!$changed) break;
    }
}



